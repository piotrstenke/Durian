namespace Durian.Info
{
	/// <summary>
	/// Defines every package of the Durian project.
	/// </summary>
	public enum DurianPackage
	{
		/// <summary>
		/// Represents the <c>Durian</c> package.
		/// </summary>
		Main,

		/// <summary>
		/// Represents the <c>Durian.Core</c> package.
		/// </summary>
		Core,

		/// <summary>
		/// Represents the <c>Durian.Core.Analyzer</c> package.
		/// </summary>
		CoreAnalyzer,

		/// <summary>
		/// Represents the <c>Durian.AnalysisServices</c> package.
		/// </summary>
		AnalysisServices,

		/// <summary>
		/// Represents the <c>Durian.TestServices</c> package.
		/// </summary>
		TestServices,

		/// <summary>
		/// Represents the <c>Durian.DefaultParam</c> package.
		/// </summary>
		DefaultParam,

		/// <summary>
		/// Represents the <c>Durian.FriendClass</c> package.
		/// </summary>
		FriendClass,

		/// <summary>
		/// Represents the <c>Durian.InterfaceTarget</c> package.
		/// </summary>
		InterfaceTargets,

		/// <summary>
		/// Represents the <c>Durian.CopyFrom</c> package.
		/// </summary>
		CopyFrom,
	}
}
